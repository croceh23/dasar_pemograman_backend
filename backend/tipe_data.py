# tipe data string
web = "Belajar Python di STIKOM Banyuwangi"
print(web)

# tipe data integer
web = 1500
print(web)

# tipe data float 
web = 99.123
print(web)

# tipe data complex number
web = 4j
print(web)

# tipe data boolean
web = True
print(web)

# tipe data list
web = ["satu","dua","tiga","satu"]
print(web)

# tipe data tuple
web = ("satu","dua","tiga","satu")
print(web)

# tipe data set 
web = {"satu","dua","tiga","empat"}
print(web)

# tipe data dictionary
web = {"satu":1,"dua":2.13,"tiga":"a","satu": True}
print(web)